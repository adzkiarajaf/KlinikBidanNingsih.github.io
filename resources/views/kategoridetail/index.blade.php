        <fieldset disabled>
            <div class="mb-3">
                <label for="disabledTextInput" class="form-label">Nama Kategori</label>
                <input type="text" id="disabledTextInput" class="form-control" value="{{ $kategori->nama_kategori }}" readonly>
            </div>
        </fieldset>
    </form>
